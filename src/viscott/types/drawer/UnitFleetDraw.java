    public UnitType type;
    public float virtualHp = 1;
    public float x = 0,y = 0;
    public float rotation = 0;
    public float altitude = 50;

    public UnitFleetDraw(UnitType type) {
        this.type = type;
    }
    public void SetPosition(float x,float y) {
        float diffX = x - this.x,
                diffY = y - this.y;
        rotation = Mathf.angle(diffX,diffY);
        this.x = x;
        this.y = y;
    }
    public void update() {
        float speed = type.speed;
        x += Mathf.sinDeg(90+rotation) * speed;
        y += Mathf.sinDeg(rotation) * speed;
    }
    public void draw() {
        Draw.z(Layer.end-10);
        Draw.rect(type.baseRegion,x,y,rotation);
        Draw.rect(type.shadowRegion,x,y,rotation);
    }
